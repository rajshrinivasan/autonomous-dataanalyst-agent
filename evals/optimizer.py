"""
Bootstrap optimizer for the LLM-as-judge eval pipeline.

Reads evals/results/*.json, groups results by case_id, computes per-dimension
rolling averages, and when any LLM-scored dimension averages below the threshold
for two consecutive run windows it appends few-shot examples from top-scoring
runs to the corresponding instructions/*.md file.

Usage:
    python -m evals.optimizer            # manual run
    pytest evals/ -m eval --optimize     # run after eval suite via conftest hook

Dimension → instructions mapping:
    sql_semantic_correctness  → sql_writer.md
    insight_quality           → analyst.md
    report_answers_question   → writer.md
"""
from __future__ import annotations

import json
import sys
from pathlib import Path

RESULTS_DIR = Path(__file__).parent / "results"
INSTRUCTIONS_DIR = Path(__file__).parent.parent / "instructions"

SCORE_THRESHOLD = 0.6       # avg below this triggers optimization
SAMPLE_SCORE_FLOOR = 0.8    # only use runs scoring at or above this as examples
MAX_FEW_SHOT_EXAMPLES = 3   # maximum examples to append
WINDOW = 5                  # number of recent result files to consider per case
CONSECUTIVE_WINDOWS = 2     # how many consecutive windows must be low to trigger

DIMENSION_TO_INSTRUCTION: dict[str, str] = {
    "sql_semantic_correctness": "sql_writer",
    "insight_quality": "analyst",
    "report_answers_question": "writer",
}

FEW_SHOT_SECTION_HEADER = "## Few-Shot Examples"
FEW_SHOT_AUTO_COMMENT = "<!-- Auto-generated by evals/optimizer.py — do not edit manually -->"


# ---------------------------------------------------------------------------
# Loading
# ---------------------------------------------------------------------------

def load_results(n: int = WINDOW) -> dict[str, list[dict]]:
    """Load the last n result files per case_id from RESULTS_DIR.

    Returns a dict mapping case_id → list of result dicts (oldest first).
    """
    if not RESULTS_DIR.exists():
        return {}

    files = sorted(RESULTS_DIR.glob("*.json"))
    grouped: dict[str, list[Path]] = {}
    for f in files:
        # filename format: {case_id}_{timestamp}.json
        parts = f.stem.rsplit("_", 1)
        case_id = parts[0] if len(parts) == 2 else f.stem
        grouped.setdefault(case_id, []).append(f)

    results: dict[str, list[dict]] = {}
    for case_id, paths in grouped.items():
        recent = paths[-n:]  # last n files (oldest first within the window)
        loaded = []
        for p in recent:
            try:
                loaded.append(json.loads(p.read_text(encoding="utf-8")))
            except (json.JSONDecodeError, OSError):
                continue
        if loaded:
            results[case_id] = loaded

    return results


# ---------------------------------------------------------------------------
# Analysis
# ---------------------------------------------------------------------------

def _extract_dimension_score(result: dict, dimension: str) -> float | None:
    """Pull the LLM score for a named dimension from a result dict."""
    for stage in result.get("stages", []):
        for check in stage.get("checks", []):
            if check.get("name") == dimension and check.get("score") is not None:
                return float(check["score"])
    return None


def find_underperforming_dimensions(
    results: dict[str, list[dict]],
    window: int = WINDOW,
    threshold: float = SCORE_THRESHOLD,
    consecutive: int = CONSECUTIVE_WINDOWS,
) -> list[str]:
    """Return dimension names that are consistently below threshold.

    A dimension is considered underperforming when its average score across the
    last *window* result files is below *threshold* in at least *consecutive*
    non-overlapping windows (or across all available data when there is only
    one window's worth).
    """
    dimension_scores: dict[str, list[float]] = {d: [] for d in DIMENSION_TO_INSTRUCTION}

    for _case_id, case_results in results.items():
        for res in case_results:
            for dim in dimension_scores:
                score = _extract_dimension_score(res, dim)
                if score is not None:
                    dimension_scores[dim].append(score)

    underperforming: list[str] = []
    for dim, scores in dimension_scores.items():
        if not scores:
            continue
        # Chunk into windows and check consecutive low averages
        chunks = [scores[i:i + window] for i in range(0, len(scores), window)]
        low_count = sum(
            1 for chunk in chunks
            if chunk and (sum(chunk) / len(chunk)) < threshold
        )
        if low_count >= consecutive:
            underperforming.append(dim)

    return underperforming


# ---------------------------------------------------------------------------
# Few-shot example generation
# ---------------------------------------------------------------------------

def sample_few_shot_examples(
    results: dict[str, list[dict]],
    dimension: str,
    n: int = MAX_FEW_SHOT_EXAMPLES,
    floor: float = SAMPLE_SCORE_FLOOR,
) -> list[str]:
    """Return formatted few-shot text blocks from top-scoring runs."""
    candidates: list[tuple[float, str, str]] = []  # (score, question, content)

    for _case_id, case_results in results.items():
        for res in case_results:
            score = _extract_dimension_score(res, dimension)
            if score is None or score < floor:
                continue
            question = res.get("question", "")
            # Extract the relevant content for each dimension
            content = _pick_content_for_dimension(res, dimension)
            if content:
                candidates.append((score, question, content))

    # Sort by score descending, take top n
    candidates.sort(key=lambda x: x[0], reverse=True)
    examples: list[str] = []
    for i, (score, question, content) in enumerate(candidates[:n], start=1):
        examples.append(
            f"### Example {i} (score: {score:.2f})\n"
            f"**Question:** {question}\n\n"
            f"{content}"
        )

    return examples


def _pick_content_for_dimension(result: dict, dimension: str) -> str:
    """Extract the stage-relevant text from a result for use as a few-shot example."""
    # For each dimension, look at the rationale in the check detail
    for stage in result.get("stages", []):
        for check in stage.get("checks", []):
            if check.get("name") == dimension:
                return check.get("detail", "")
    return ""


# ---------------------------------------------------------------------------
# Instruction file modification
# ---------------------------------------------------------------------------

def append_few_shots_to_instruction(instruction_key: str, examples: list[str]) -> None:
    """Append examples to instructions/<instruction_key>.md.

    If a few-shot section already exists, replaces it.  Otherwise appends.
    """
    path = INSTRUCTIONS_DIR / f"{instruction_key}.md"
    if not path.exists():
        print(f"[optimizer] Warning: {path} not found — skipping", file=sys.stderr)
        return

    content = path.read_text(encoding="utf-8")
    new_section = (
        f"\n\n{FEW_SHOT_SECTION_HEADER}\n"
        f"{FEW_SHOT_AUTO_COMMENT}\n\n"
        + "\n\n".join(examples)
    )

    # Remove any existing auto-generated section
    if FEW_SHOT_SECTION_HEADER in content:
        idx = content.index(FEW_SHOT_SECTION_HEADER)
        content = content[:idx].rstrip()

    path.write_text(content + new_section, encoding="utf-8")


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------

def run_optimizer(n: int = WINDOW) -> list[str]:
    """Run the bootstrap optimizer.  Returns list of modified instruction keys."""
    results = load_results(n)
    if not results:
        print("[optimizer] No eval results found in evals/results/", file=sys.stderr)
        return []

    underperforming = find_underperforming_dimensions(results)
    if not underperforming:
        return []

    modified: list[str] = []
    for dim in underperforming:
        instruction_key = DIMENSION_TO_INSTRUCTION.get(dim)
        if not instruction_key:
            continue
        examples = sample_few_shot_examples(results, dim)
        if not examples:
            continue
        append_few_shots_to_instruction(instruction_key, examples)
        modified.append(instruction_key)
        print(
            f"[optimizer] Appended {len(examples)} few-shot example(s) to "
            f"instructions/{instruction_key}.md (dimension: {dim})",
            file=sys.stderr,
        )

    return modified


if __name__ == "__main__":
    modified = run_optimizer()
    if modified:
        print(f"Updated instruction files: {modified}")
        print("Re-run the eval suite to measure improvement.")
    else:
        print("No instruction files needed updating.")
